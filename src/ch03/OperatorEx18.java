package ch03;

public class OperatorEx18 {
    public static void main(String[] args) {
        double pi = 3.141592;

        System.out.println("Math.round(pi) : " + Math.round(pi));
        System.out.println("Math.round(3.5111) : " + Math.round(3.5111));
        System.out.println("Math.ceil(pi) : " + Math.ceil(pi));
        System.out.println("Math.ceil(3.000000) : " + Math.ceil(3.000000));
        System.out.println("Math.ceil(3.0001) : " + Math.ceil(3.0001));
        System.out.println("Math.floor(3.0001) : " + Math.floor(3.0001));
        System.out.println("Math.floor(3.99999) : " + Math.floor(3.99999));
    }
}
